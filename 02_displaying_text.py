# Single quotes
print('The capybara likes to live in groups')

# Double quotes
print("The capybara can swim")

# The problem with using single quotes (uncomment to see)
# print('The capybara is the world's largest rodent')
# In order to avoid that problem, you can escape the single quote by adding a "\"
print('The capybara is the world\'s largest rodent')

# For Python, it doesn't really matter if you use single or double quotes
# Just pick one way and stick with it (double)

# Multiple print commands
print('The capybara is the worlds largest rodent')
print('The capybara likes to live in groups')
print("The capybara can swim")

# One single print command
print('The capybara is the worlds largest rodent\nThe capybara likes to live in groups\nThe capybara can swim')

# Or another way to print multiple lines
print("""She loves you yeah yeah yeah
She loves you yeah yeah yeah
She loves you yeah yeah yeah""")

print('''She loves you yeah yeah yeah
She loves you yeah yeah yeah
She loves you yeah yeah yeah''')

print('here is a double quote: "' + '\n' + "And here is a single quote: '")
print("I really like the first one there, but it seems to make more sense to me \" ")
print("Can I just print a \ in the middle of the string?")

# I have to add a \ before the \news to escape the \n
